Pareto Principle (80/20 Rule)

Pareto Principle (80/20 Rule) states that for many outcomes, roughly 80% of consequences come from 20% of causes.
Example:
- 80% of productivity from 20% of the employees
- 80% of sales come from 20% of the clients
- 80% of decisions in a meeting are made in 20% of the time
*/


